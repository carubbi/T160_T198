// Aula 6 - Exemplo 3 (condicional composta)
// Declaracao de variaveis
let condicao;
let mensagem;

// Entrada
condicao = prompt("Digite o valor logico da condicao (true/false):"); // true

// Processamento
if (condicao == "true") {
  mensagem = "caminho verdadeiro";
} else {
  mensagem = "caminho falso";
}

// Saida
console.log(mensagem);
